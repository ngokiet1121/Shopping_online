package models;

/**
 *
 * @author ADMIN
 */
public class Banner {

    public int id;
    public String banner;

    public Banner() {
    }

    public Banner(String banner) {
        this.banner = banner;
    }

    public Banner(int id, String banner) {
        this.id = id;
        this.banner = banner;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBanner() {
        return banner;
    }

    public void setBanner(String banner) {
        this.banner = banner;
    }

    @Override
    public String toString() {
        return banner;
    }
}
