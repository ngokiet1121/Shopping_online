        String email = request.getParameter("email");
        String password = request.getParameter("password");
        String passMD5 = helper.MD5Generator.generate(password);
        Admin admin = new Admin(email,passMD5);
        
        if(Controller.controller.checkLoginA(admin)){
            request.getSession().setAttribute("admin", adminDao.find(email));          
            response.sendRedirect("admin/admin.jsp");
        }else{           
            request.getRequestDispatcher("login-admin.jsp").forward(request, response);
        }
